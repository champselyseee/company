/** Разбирает ?demo=... → тип работы для демо-результата, либо null. */
export function getDemoType(): WorkType | null {
  const v = new URLSearchParams(window.location.search).get('demo')
  if (!v) return null
  if (v === 'email' || v === 'composition' || v === 'essay') return v
  return 'essay' // ?demo=1 → эссе по умолчанию
}
